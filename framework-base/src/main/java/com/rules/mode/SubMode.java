public class SubMode {

    private Double chargeWeight;

    private Double freightWeight;

    private String address;

    private String phone;

    public Double getChargeWeight() {
        return chargeWeight;
    }

    public void setChargeWeight(Double chargeWeight) {
        this.chargeWeight = chargeWeight;
    }

    public Double getFreightWeight() {
        return freightWeight;
    }

    public void setFreightWeight(Double freightWeight) {
        this.freightWeight = freightWeight;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
